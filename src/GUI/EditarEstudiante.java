    private void BuscarMatriculaFieldEditarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BuscarMatriculaFieldEditarKeyReleased
       String matricula = BuscarMatriculaFieldEditar.getText().trim();
    if (timer != null) {
        timer.stop();
    }
    if (!matricula.isEmpty()) {

        timer = new Timer(400, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (!matricula.equals(lastQuery)) {
                    DefaultTableModel modelo = (DefaultTableModel) TablaEstudiantesEditar.getModel();
                    try {
                        ConsultasSQL.BuscarEstudianteEditar(modelo, matricula); 
                        lastQuery = matricula;
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(VerEstudiantes.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
        timer.setRepeats(false);
        timer.start();
    } else {
        // Si el campo está vacío, mostrar todos los estudiantes
        DefaultTableModel modelo = (DefaultTableModel) TablaEstudiantesEditar.getModel();
        try {
            ConsultasSQL.BuscarEstudianteEditar(modelo, matricula); 
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VerEstudiantes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    }//GEN-LAST:event_BuscarMatriculaFieldEditarKeyReleased

    
