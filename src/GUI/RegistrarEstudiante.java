    private void GuardarEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarEstudianteActionPerformed

    String matricula = MatriculaRegistro.getText();
    String nombres = NombresRegistro.getText();
    String apellidos = ApellidosRegistro.getText();
    String numero = NumeroRegistro.getText();
    String modulo = (String) ModuloRegistro.getSelectedItem();
    String habitacion = (String) HabitacionRegistro.getSelectedItem();

    System.out.println(" " + matricula + " " + nombres + " " + apellidos + " " + numero + " " + modulo + " " + habitacion);

    // Verificar si hay datos faltantes antes de intentar guardar
    if (matricula.equals("") || nombres.equals("") || apellidos.equals("") || numero.equals("") || 
        habitacion.equals("") || modulo.equals("Seleccionar") || habitacion.equals("Seleccionar")) {
        JOptionPane.showMessageDialog(this, "Faltan datos. Por favor, complete todos los campos.");
        return; // Salir del método si faltan datos
    }

    // Llamar al método GuardarEstudiante y verificar si se guardó correctamente
    boolean guardadoExitosamente = false; // Bandera para indicar si se guardaron los datos correctamente
    try {
        guardadoExitosamente = estudianteControlador.GuardarEstudiante(matricula, nombres, apellidos, numero, modulo, habitacion); 
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Error en el formato del módulo. Asegúrate de seleccionar un valor válido.");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al guardar el estudiante: " + ex.getMessage());
        Logger.getLogger(RegistrarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
    }

    if (!guardadoExitosamente) {
        MatriculaRegistro.setText("");
        NombresRegistro.setText("");
        ApellidosRegistro.setText("");
        NumeroRegistro.setText("");
        ModuloRegistro.setSelectedIndex(0);
        HabitacionRegistro.setSelectedIndex(0);
        System.out.println("Campos limpiados y estudiante registrado.");
    }
    }//GEN-LAST:event_GuardarEstudianteActionPerformed

